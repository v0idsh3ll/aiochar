def post_content_validation(content):
    if len(content) > 1024:
        raise InvalidPostContent

def poll_content_validation(content):
    if len(content) > 200:
        raise InvalidPollContent

def poll_options_validation(poll_options):
    if len(poll_options) > 4:
        raise InvalidPollOptions

    for poll_option in poll_options:
        post_content_validation(poll_option)

def post_format_validation(content, poll_options):
    if not content and not poll_options:
        raise InvalidPostFormat

    post_content_validation(content)

    poll_options_validation(poll_options)